    <div class="row">
        <div class="b-green flex-heading collapsible"><h2 class="green">Hvordan fungerer en app?</h2></div>
        <p class="collapseContent">Apps kendes typisk som små tilføjelsesprogrammer til eksempelvis smartphones. Mange kender apps som Facebook til mobilen, spillet Candy Crush og funktioner som lommeregner eller lommelygte. Det er altså små programmer eller software, som fungerer via en computer eller et "Application Programming Interface" (API). Der findes en endeløs mængde af apps inden for en masse forskellige områder; bl.a. hjælp til regnskab, bogføring, billedredigering, projektstyring, vejvisning og meget andet. Kort sagt, så er der næsten ingen grænser for hvad en app kan bruges til.</p>
    </div>

    &nbsp;

    <div class="row">
        <div class="b-blue flex-heading collapsible"><h2 class="blue">Hvad kan i opnå med en app?</h2></div>
        <p class="collapseContent">Mobilteknologien er i en rivende udvikling. Smartphone produkter som iPhone og Android har stor succes, og forbrugermarkedet driver derfor app-revolutionen. For virksomheder ligger der mange gemte muligheder i denne udvikling, men det kræver både vilje, mod og evner at ride med på bølgen. Over de sidste mange år har flere og flere mennesker dropper deres stationære og bærbare computere til fordel for tablets og smartphones. I begyndelsen af 2018 kom 51,2% af den globale internettrafik fra mobile enheder.</p>
    </div>

    &nbsp;

    <div class="row">
        <div class="b-red flex-heading collapsible"><h2 class="red">Tilkobling til eksisterende systemer</h2></div>
        <p class="collapseContent">Application Programming Interface - forkortet API - gør det muligt for forskellige programmer at ”tale” sammen. Det betyder bl.a. at du med et API kan udveksle data på tværs af programmer. <br> Fordelen ved et API er først og fremmest, at det letter arbejdet, fordi du slipper for manuelt at overføre oplysninger, som eksempelvis fra din webshop til dit regnskabsprogram.
            Et godt API gør det desuden lettere at udvikle nye programmer til din virksomhed, fordi det kan forbinde og give adgang til alle de oplysninger og ”byggeklodser” som dine systemer allerede bruger. Når alle oplysninger er tilgængelige og samlet, kan programmøren lettere udvikle og få tingene til at arbejde sammen.</p>
